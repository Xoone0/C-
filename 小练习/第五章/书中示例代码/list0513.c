/*
	求4名学生在两次考试中3门课程的总分并显示
*/

#include <stdio.h>

int main(void)
{
	int i, j, l;
	int tensu1[4][3][3] = {{91, 63, 78}, {67, 72, 46}, {89, 34, 53}, {32, 54, 34}};
	int tensu2[4][3][3] = {{97, 67, 82}, {73, 43, 46}, {97, 56, 21}, {85, 46, 35}};
	int sum[4][3];			/* 总分 */

	/* 求两次考试的分数之和 */
	for (i = 0; i < 4; i++) {							/* 4名学生的 */
		for (j = 0; j < 3; j++)							/* 3门课程的 */
			for (l = 0; l < 3; l++)
			{
				sum[i][j][l] = tensu1[i][j][l] + tensu2[i][j][l];	/* 两次的分数相加 */
			}
	}

	/* 显示第一次考试的分数 */
	puts("第一次考试的分数");
	for (i = 0; i < 4; i++) {
		for (j = 0; j < 3; j++)
			for (l = 0; l < 3; l++)
			{
				printf("%4d", tensu1[i][j][l]);
			}
		putchar('\n');
	}

	/* 显示第二次考试的分数 */
	puts("第二次考试的分数");
	for (i = 0; i < 4; i++) {
		for (j = 0; j < 3; j++)
			for (l = 0; l < 3; l++)
			{
				printf("%4d", tensu1[i][j][l]);
			}
		putchar('\n');
	}

	/* 显示总分 */
	puts("总分");
	for (i = 0; i < 4; i++) {
		for (j = 0; j < 3; j++)
			printf("%4d", sum[i][j][l]);
		putchar('\n');
	}

	return 0;
}
